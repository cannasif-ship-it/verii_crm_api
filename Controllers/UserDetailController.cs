using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Authorization;
using cms_webapi.DTOs;
using cms_webapi.Interfaces;

namespace cms_webapi.Controllers
{
    [ApiController]
    [Route("api/[controller]")]
    [Authorize]
    public class UserDetailController : ControllerBase
    {
        private readonly IUserDetailService _service;
        private readonly ILocalizationService _localizationService;

        public UserDetailController(IUserDetailService service, ILocalizationService localizationService)
        {
            _service = service;
            _localizationService = localizationService;
        }

        [HttpGet]
        public async Task<IActionResult> GetAll([FromQuery] PagedRequest request)
        {
            var result = await _service.GetPagedAsync(request);
            return StatusCode(result.StatusCode, result);
        }

        [HttpGet("user/{userId}")]
        public async Task<IActionResult> GetByUserId(long userId)
        {
            var result = await _service.GetByUserIdAsync(userId);
            return StatusCode(result.StatusCode, result);
        }

        [HttpGet("{id}")]
        public async Task<IActionResult> GetById(long id)
        {
            var result = await _service.GetByIdAsync(id);
            return StatusCode(result.StatusCode, result);
        }

        [HttpPost]
        public async Task<IActionResult> Create([FromBody] CreateUserDetailDto dto)
        {
            if (!ModelState.IsValid)
            {
                return StatusCode(400, ApiResponse<UserDetailDto>.ErrorResult(
                    _localizationService.GetLocalizedString("UserDetailController.InvalidModelState"),
                    _localizationService.GetLocalizedString("UserDetailController.InvalidModelStateExceptionMessage", ModelState?.ToString() ?? string.Empty),
                    400));
            }

            var result = await _service.CreateAsync(dto);
            return StatusCode(result.StatusCode, result);
        }

        [HttpPut("{id}")]
        public async Task<IActionResult> Update(long id, [FromBody] UpdateUserDetailDto dto)
        {
            if (!ModelState.IsValid)
            {
                return StatusCode(400, ApiResponse<UserDetailDto>.ErrorResult(
                    _localizationService.GetLocalizedString("UserDetailController.InvalidModelState"),
                    _localizationService.GetLocalizedString("UserDetailController.InvalidModelStateExceptionMessage", ModelState?.ToString() ?? string.Empty),
                    400));
            }

            var result = await _service.UpdateAsync(id, dto);
            return StatusCode(result.StatusCode, result);
        }

        [HttpDelete("{id}")]
        public async Task<IActionResult> Delete(long id)
        {
            var result = await _service.DeleteAsync(id);
            return StatusCode(result.StatusCode, result);
        }

        [HttpPost("users/{userId}/profile-picture")]
        [Consumes("multipart/form-data")]
        public async Task<IActionResult> UploadProfilePicture(
            [FromRoute] long userId,
            IFormFile file) // ‚ùó [FromForm] YOK - IFormFile otomatik olarak form'dan bind edilir
        {
            var result = await _service.UploadProfilePictureAsync(userId, file);
            return StatusCode(result.StatusCode, result);
        }
    }
}
